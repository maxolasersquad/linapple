void SSC_Reset(SuperSerialCard* pSSC) {
  GetDIPSW(pSSC);
  pSSC->m_vRecvBytes = 0;
  pSSC->m_bTxIrqEnabled = false;
  pSSC->m_bRxIrqEnabled = false;
  pSSC->m_bWrittenTx = false;
  pSSC->m_vbCommIRQ = false;
  pSSC->m_uCommandByte = 0xFF; // Ensure first write always triggers UpdateCommState
}

void SSC_Initialize(SuperSerialCard* pSSC, uint8_t* pCxRomPeripheral, uint32_t uSlot) {
  const uint32_t SSC_FW_SIZE = 2 * 1024;
  const uint32_t SSC_SLOT_FW_SIZE = 256;
  const uint32_t SSC_SLOT_FW_OFFSET = 7 * 256;

  memcpy(pCxRomPeripheral + static_cast<size_t>(uSlot * 256), SSC_rom.data() + SSC_SLOT_FW_OFFSET, SSC_SLOT_FW_SIZE);

  // Expansion ROM
  if (pSSC->m_pExpansionRom == nullptr) {
    pSSC->m_pExpansionRom = new uint8_t[SSC_FW_SIZE];
    if (pSSC->m_pExpansionRom) {
      memcpy(pSSC->m_pExpansionRom, SSC_rom.data(), SSC_FW_SIZE);
    }
  }

  RegisterIoHandler(uSlot, &SSC_IORead, &SSC_IOWrite, nullptr, nullptr, pSSC,
                    pSSC->m_pExpansionRom);
}

void SSC_Destroy(SuperSerialCard* pSSC) {
  SSC_Reset(pSSC);
  delete[] pSSC->m_pExpansionRom;
  pSSC->m_pExpansionRom = nullptr;
}

static void GetDIPSW(SuperSerialCard* pSSC) {
  SetDIPSWDefaults(pSSC);

  pSSC->m_uBaudRate = pSSC->m_DIPSWCurrent.uBaudRate;
  pSSC->m_eStopBits = pSSC->m_DIPSWCurrent.eStopBits;
  pSSC->m_uByteSize = pSSC->m_DIPSWCurrent.uByteSize;
  pSSC->m_eParity = pSSC->m_DIPSWCurrent.eParity;
  pSSC->m_uControlByte = GenerateControl(pSSC);
  pSSC->m_uCommandByte = 0x00;
}

static void SetDIPSWDefaults(SuperSerialCard* pSSC) {
  pSSC->m_DIPSWCurrent = g_DIPSWDefault;
}

static auto GenerateControl(SuperSerialCard* pSSC) -> uint8_t {
  const uint32_t CLK = 1;  // Internal
  uint32_t bmByteSize = (8 - pSSC->m_uByteSize);
  assert(bmByteSize <= 3);

  uint32_t StopBit = 0;
  if (((pSSC->m_uByteSize == 8) && (pSSC->m_eParity != SSC_PARITY_NONE)) || (pSSC->m_eStopBits != SSC_STOP_BITS_1)) {
    StopBit = 1;
  } else {
    StopBit = 0;
  }

  return (StopBit << 7) | (bmByteSize << 5) | (CLK << 4) | BaudRateToIndex(pSSC->m_uBaudRate);
}

static auto BaudRateToIndex(uint32_t uBaudRate) -> uint32_t {
  switch (uBaudRate) {
    case SSC_B110:   return BAUD_110_INDEX;
    case SSC_B300:   return BAUD_300_INDEX;
    case SSC_B600:   return BAUD_600_INDEX;
    case SSC_B1200:  return BAUD_1200_INDEX;
    case SSC_B2400:  return BAUD_2400_INDEX;
    case SSC_B4800:  return BAUD_4800_INDEX;
    case SSC_B9600:  return BAUD_9600_INDEX;
    case SSC_B19200: return BAUD_19200_INDEX;
  }
  return BAUD_9600_INDEX; // Default 9600
}

static void UpdateCommState(SuperSerialCard* pSSC) {
  if (SSCFrontend_IsActive()) {
      SSCFrontend_UpdateState(pSSC->m_uBaudRate, pSSC->m_uByteSize, pSSC->m_eParity, pSSC->m_eStopBits);
  }
}

auto SSC_IORead(uint16_t PC, uint16_t uAddr, uint8_t bWrite, uint8_t uValue, uint32_t nCyclesLeft) -> uint8_t {
  uint32_t uSlot = ((uAddr & 0xff) >> 4) - 8;
  auto *pSSC = static_cast<SuperSerialCard *>(MemGetSlotParameters(uSlot));

  switch (uAddr & ADDR_NIBBLE_MASK) {
    case SSC_OFFSET_DIPSW1: return CommDipSw(pSSC, PC, uAddr, bWrite, uValue, nCyclesLeft);
    case SSC_OFFSET_DIPSW2: return CommDipSw(pSSC, PC, uAddr, bWrite, uValue, nCyclesLeft);
    case SSC_OFFSET_DATA: return CommReceive(pSSC, PC, uAddr, bWrite, uValue, nCyclesLeft);
    case SSC_OFFSET_STATUS: return CommStatus(pSSC, PC, uAddr, bWrite, uValue, nCyclesLeft);
    case SSC_OFFSET_COMMAND: return CommCommand(pSSC, PC, uAddr, bWrite, uValue, nCyclesLeft);
    case SSC_OFFSET_CONTROL: return CommControl(pSSC, PC, uAddr, bWrite, uValue, nCyclesLeft);
    default:  return IO_Null(PC, uAddr, bWrite, uValue, nCyclesLeft);
  }
}

auto SSC_IOWrite(uint16_t PC, uint16_t uAddr, uint8_t bWrite, uint8_t uValue, uint32_t nCyclesLeft) -> uint8_t {
  uint32_t uSlot = ((uAddr & 0xff) >> 4) - 8;
  auto *pSSC = static_cast<SuperSerialCard *>(MemGetSlotParameters(uSlot));

  switch (uAddr & ADDR_NIBBLE_MASK) {
    case SSC_OFFSET_DATA: return CommTransmit(pSSC, PC, uAddr, bWrite, uValue, nCyclesLeft);
    case SSC_OFFSET_STATUS: return CommStatus(pSSC, PC, uAddr, bWrite, uValue, nCyclesLeft);
    case SSC_OFFSET_COMMAND: return CommCommand(pSSC, PC, uAddr, bWrite, uValue, nCyclesLeft);
    case SSC_OFFSET_CONTROL: return CommControl(pSSC, PC, uAddr, bWrite, uValue, nCyclesLeft);
    default:  return IO_Null(PC, uAddr, bWrite, uValue, nCyclesLeft);
  }
}

static auto CommCommand(SuperSerialCard* pSSC, uint16_t, uint16_t, uint8_t write, uint8_t value, uint32_t) -> uint8_t {
  if (write && (value != pSSC->m_uCommandByte)) {
    pSSC->m_uCommandByte = value;
    if (pSSC->m_uCommandByte & COMMAND_PARITY_ENABLED_BIT) {
      switch (pSSC->m_uCommandByte & COMMAND_PARITY_MODE_MASK) {
        case 0x00 : pSSC->m_eParity = SSC_PARITY_ODD; break;
        case 0x40 : pSSC->m_eParity = SSC_PARITY_EVEN; break;
        case 0x80 : pSSC->m_eParity = SSC_PARITY_MARK; break;
        case 0xC0 : pSSC->m_eParity = SSC_PARITY_SPACE; break;
      }
    } else {
      pSSC->m_eParity = SSC_PARITY_NONE;
    }

    switch (pSSC->m_uCommandByte & COMMAND_TX_CONTROL_MASK) {
      case 0 << 2: pSSC->m_bTxIrqEnabled = false; break;
      case 1 << 2: pSSC->m_bTxIrqEnabled = true; break;
      case 2 << 2: pSSC->m_bTxIrqEnabled = false; break;
      case 3 << 2: pSSC->m_bTxIrqEnabled = false; break;
    }
    pSSC->m_bRxIrqEnabled = ((pSSC->m_uCommandByte & COMMAND_RX_IRQ_DISABLED_BIT) == 0);
    UpdateCommState(pSSC);
  }
  return pSSC->m_uCommandByte;
}

static auto CommControl(SuperSerialCard* pSSC, uint16_t, uint16_t, uint8_t write, uint8_t value, uint32_t) -> uint8_t {
  if (write && (value != pSSC->m_uControlByte)) {
    pSSC->m_uControlByte = value;
    switch (pSSC->m_uControlByte & CONTROL_BAUD_RATE_MASK) {
      case 0x00: case 0x01: case 0x02: case 0x03: case 0x04: case BAUD_110_INDEX:
        pSSC->m_uBaudRate = SSC_B110; break;
      case BAUD_300_INDEX: pSSC->m_uBaudRate = SSC_B300; break;
      case BAUD_600_INDEX: pSSC->m_uBaudRate = SSC_B600; break;
      case BAUD_1200_INDEX: pSSC->m_uBaudRate = SSC_B1200; break;
      case 0x09: case BAUD_2400_INDEX: pSSC->m_uBaudRate = SSC_B2400; break;
      case 0x0B: case BAUD_4800_INDEX: pSSC->m_uBaudRate = SSC_B4800; break;
      case 0x0D: case BAUD_9600_INDEX: pSSC->m_uBaudRate = SSC_B9600; break;
      case BAUD_19200_INDEX: pSSC->m_uBaudRate = SSC_B19200; break;
    }

    switch (pSSC->m_uControlByte & CONTROL_BYTE_SIZE_MASK) {
      case 0x00: pSSC->m_uByteSize = 8; break;
      case 0x20: pSSC->m_uByteSize = 7; break;
      case 0x40: pSSC->m_uByteSize = 6; break;
      case 0x60: pSSC->m_uByteSize = 5; break;
    }

    if (pSSC->m_uControlByte & CONTROL_STOP_BITS_BIT) {
      if ((pSSC->m_uByteSize == 8) && (pSSC->m_eParity != SSC_PARITY_NONE)) { pSSC->m_eStopBits = SSC_STOP_BITS_1;
      } else if ((pSSC->m_uByteSize == 5) && (pSSC->m_eParity == SSC_PARITY_NONE)) { pSSC->m_eStopBits = SSC_STOP_BITS_1_5;
      } else { pSSC->m_eStopBits = SSC_STOP_BITS_2;
}
    } else {
      pSSC->m_eStopBits = SSC_STOP_BITS_1;
    }
    UpdateCommState(pSSC);
  }
  return pSSC->m_uControlByte;
}

static auto CommReceive(SuperSerialCard* pSSC, uint16_t, uint16_t, uint8_t, uint8_t, uint32_t) -> uint8_t {
  uint8_t result = 0;
  if (pSSC->m_vRecvBytes) {
    result = pSSC->m_RecvBuffer[0];
    pSSC->m_vRecvBytes--;
    for (uint32_t i = 0; i < pSSC->m_vRecvBytes; ++i) {
        pSSC->m_RecvBuffer[i] = pSSC->m_RecvBuffer[i+1];
    }
  }
  return result;
}

static auto CommTransmit(SuperSerialCard* pSSC, uint16_t, uint16_t, uint8_t, uint8_t value, uint32_t) -> uint8_t {
  SSCFrontend_SendByte(value);
  pSSC->m_bWrittenTx = true; // Transmit interrupt pending
  if (pSSC->m_bTxIrqEnabled) {
      pSSC->m_vbCommIRQ = true;
      CpuIrqAssert(IS_SSC);
  }
  return 0;
}

static constexpr uint8_t ST_PARITY_ERR = 1 << 0;
static constexpr uint8_t ST_FRAMING_ERR = 1 << 1;
static constexpr uint8_t ST_OVERRUN_ERR = 1 << 2;
static constexpr uint8_t ST_RX_FULL = 1 << 3;
static constexpr uint8_t ST_TX_EMPTY = 1 << 4;
static constexpr uint8_t ST_DCD = 1 << 5;
static constexpr uint8_t ST_DSR = 1 << 6;
static constexpr uint8_t ST_IRQ = 1 << 7;

static auto CommStatus(SuperSerialCard* pSSC, uint16_t, uint16_t, uint8_t write, uint8_t, uint32_t) -> uint8_t {
  if (write) {
    // Programmed Reset: Datasheet pg 6-8
    // Clears bits 0-4 of Command Register
    pSSC->m_uCommandByte &= 0xE0;
    pSSC->m_bTxIrqEnabled = false;
    pSSC->m_bRxIrqEnabled = true; // Bit 1 = 0 means enabled for 6551

    // Status bits 0-3 cleared, bit 4 (TDRE) set, IRQ cleared
    pSSC->m_bWrittenTx = false;
    pSSC->m_vRecvBytes = 0;
    pSSC->m_vbCommIRQ = false;
    CpuIrqDeassert(IS_SSC);
    return 0;
  }

  bool bTxIRQ = pSSC->m_bTxIrqEnabled && pSSC->m_bWrittenTx;
  bool bRxIRQ = pSSC->m_bRxIrqEnabled && pSSC->m_vRecvBytes;
  bool bIRQ = bTxIRQ || bRxIRQ;

  // Reading the Status Register clears the TDRE interrupt bit (m_bWrittenTx)
  pSSC->m_bWrittenTx = false;

  uint8_t uStatus = ST_TX_EMPTY | (pSSC->m_vRecvBytes ? ST_RX_FULL : 0x00) | (bIRQ ? ST_IRQ : 0x00);
  if (!SSCFrontend_IsActive()) uStatus |= ST_DSR | ST_DCD;

  // Deassert IRQ only if no other interrupt sources are active
  if (!(pSSC->m_bRxIrqEnabled && pSSC->m_vRecvBytes)) {
      CpuIrqDeassert(IS_SSC);
      pSSC->m_vbCommIRQ = false;
  }

  return uStatus;
}

static auto CommDipSw(SuperSerialCard* pSSC, uint16_t, uint16_t addr, uint8_t, uint8_t, uint32_t) -> uint8_t {
  uint8_t sw = 0;
  if ((addr & ADDR_NIBBLE_MASK) == 1) {
    sw = (BaudRateToIndex(pSSC->m_DIPSWCurrent.uBaudRate) << 4) | pSSC->m_DIPSWCurrent.eFirmwareMode;
  } else if ((addr & ADDR_NIBBLE_MASK) == 2) {
    uint8_t INT = pSSC->m_DIPSWCurrent.eStopBits == SSC_STOP_BITS_2 ? 1 : 0;
    uint8_t DCD = pSSC->m_DIPSWCurrent.uByteSize == 7 ? 1 : 0;
    uint8_t RDR = 0, OVR = 0;
    switch (pSSC->m_DIPSWCurrent.eParity) {
      case SSC_PARITY_ODD:  RDR = 0; OVR = 1; break;
      case SSC_PARITY_EVEN: RDR = 1; OVR = 1; break;
      default:              RDR = 0; OVR = 0; break;
    }
    uint8_t FE = pSSC->m_DIPSWCurrent.bLinefeed ? 1 : 0;
    uint8_t PE = pSSC->m_DIPSWCurrent.bInterrupts ? 1 : 0;
    sw = (INT << 7) | (DCD << 5) | (RDR << 3) | (OVR << 2) | (FE << 1) | (PE << 0);
  }
  return sw;
}

void SSC_PushRxByte(SuperSerialCard* pSSC, uint8_t byte) {
    if (pSSC->m_vRecvBytes < uRecvBufferSize) {
        pSSC->m_RecvBuffer[pSSC->m_vRecvBytes++] = byte;
        if (pSSC->m_bRxIrqEnabled) {
            pSSC->m_vbCommIRQ = true;
            CpuIrqAssert(IS_SSC);
        }
    }
}

auto SSC_GetSnapshot(SuperSerialCard* pSSC, SS_IO_Comms *pSS) -> uint32_t {
  pSS->baudrate = pSSC->m_uBaudRate;
  pSS->bytesize = pSSC->m_uByteSize;
  pSS->commandbyte = pSSC->m_uCommandByte;
  pSS->comminactivity = 0;
  pSS->controlbyte = pSSC->m_uControlByte;
  pSS->parity = pSSC->m_eParity;
  memcpy(pSS->recvbuffer, pSSC->m_RecvBuffer, uRecvBufferSize);
  pSS->recvbytes = pSSC->m_vRecvBytes;
  pSS->stopbits = pSSC->m_eStopBits;
  return 0;
}

auto SSC_SetSnapshot(SuperSerialCard* pSSC, SS_IO_Comms *pSS) -> uint32_t {
  pSSC->m_uBaudRate = pSS->baudrate;
  pSSC->m_uByteSize = pSS->bytesize;
  pSSC->m_uCommandByte = pSS->commandbyte;
  pSSC->m_uControlByte = pSS->controlbyte;
  pSSC->m_eParity = static_cast<SscParity>(pSS->parity);
  memcpy(pSSC->m_RecvBuffer, pSS->recvbuffer, uRecvBufferSize);
  pSSC->m_vRecvBytes = pSS->recvbytes;
  pSSC->m_eStopBits = static_cast<SscStopBits>(pSS->stopbits);
  return 0;
}
